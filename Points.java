package yonsei.ac.wedlibrary;

public class Points {
	public String songTitle;
	public String points;
	public String date;
	public String name;
	public int rank;
	
	public Points(String songTitle, String points, String date, String name, int rank) {
		super();
		this.songTitle = songTitle;
		this.points = points;
		this.date = date;
		this.name = name;
		this.rank = rank;
	}
}
